package jung.spring.vo;

public class UserInfoVO {
	private int count;
	private String name;
	private int month;
	private int day;
	private int day_end;
	
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	public int getMonth() {
		return month;
	}
	public void setMonth(int month) {
		this.month = month;
	}
	
	
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	
	public int getDay_end() {
		return day_end;
	}
	public void setDay_end(int day_end) {
		this.day_end = day_end;
	}
	
}